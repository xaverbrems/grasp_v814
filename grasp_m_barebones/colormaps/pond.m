function [cmap] = cegerd2(maplength);
% ColEdit function [cmap] = cegerd2(maplength);
%
% colormap m-file written by ColEdit
% version 1.1 on 05-Jul-2000
%
% input  :	[maplength]	[64]	- colormap length
%
% output :	cmap			- colormap RGB-value array
 
% set red points
r = [ [];...
    [0 0.031915];...
    [0.3175 0.097087];...
    [0.39744 0.085106];...
    [0.44872 0.84043];...
    [0.46923 0.94681];...
    [0.4921 1];...
    [0.5079 1];...
    [0.7721 0.86408];...
    [1 1];...
    [] ];
 
% set green points
g = [ [];...
    [0 0.06383];...
    [0.3333 0.95745];...
    [0.4921 1];...
    [0.72564 0.82979];...
    [0.8641 0.47872];...
    [1 0.021277];...
    [] ];
 
% set blue points
b = [ [];...
    [0 0.92553];...
    [0.1587 0.6408];...
    [0.38462 0.41489];...
    [0.5079 1];...
    [0.6 0.18085];...
    [0.6825 0.11702];...
    [1 0.021277];...
    [] ];
% ColEditInfoEnd
 
% get colormap length
if nargin==1 
  if length(maplength)==1
    if maplength<1
      maplength = 64;
    elseif maplength>256
      maplength = 256;
    elseif isinf(maplength)
      maplength = 64;
    elseif isnan(maplength)
      maplength = 64;
    end
  end
else
  maplength = 64;
end
 
% interpolate colormap
np = linspace(0,1,maplength);
rr = interp1(r(:,1),r(:,2),np,'linear');
gg = interp1(g(:,1),g(:,2),np,'linear');
bb = interp1(b(:,1),b(:,2),np,'linear');
 
% compose colormap
cmap = [rr(:),gg(:),bb(:)];
